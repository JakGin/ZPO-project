package org.example.attendancelist.model;

/**
 * Enum representing different attendance statuses.
 */
public enum Status {

    /**
     * Student is present during the class.
     */
    PRESENT,

    /**
     * Student is absent during the class.
     */
    ABSENT,

    /**
     * Student is excused for the class.
     */
    EXCUSED,

    /**
     * Student is late for the class.
     */
    LATE
}
